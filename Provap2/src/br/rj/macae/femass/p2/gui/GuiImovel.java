 private void limparCampos() {
        txtNome.setText("");
        txtDescricao.setText("");
        txtAreaTotal.setText("");
        txtAreaConst.setText("");
        tblImoveis.clearSelection();   
        try {
            atualizarComboImovel();
            cboTipo.setSelectedIndex(-1);
        } catch (SQLException ex) {
            Logger.getLogger(GuiImovel.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        
    
    }

    private void habilitarCampos() {
        pNovo.setVisible(true);
        txtNome.requestFocus(true);
        btnGravar.setVisible(true);     
        btnCancelar.setVisible(true); 
        tblImoveis.setEnabled(false);
    }
    private void desabilitarCampos() {
        pNovo.setVisible(false);
        btnGravar.setVisible(false);     
        btnCancelar.setVisible(false);     
        tblImoveis.setEnabled(true);
        
    }
    
    private void atualizarLista(){
           ImovelControle controle = new ImovelControle();
        try {
            controle.atualizarLista(tblImoveis);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }        
    }
private void atualizarComboImovel() throws SQLException {
        ImovelControle controle = new ImovelControle();
        List tipos = controle.listarTipo();
        cboTipo.removeAllItems();
        for(Object o:tipos){
            cboTipo.addItem(o);
        }
    }
