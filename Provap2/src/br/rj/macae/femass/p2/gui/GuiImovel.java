    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        if(tblImoveis.getSelectedRow()>=0){
            ImovelControle controle = new ImovelControle();
            Object valor=((DefaultTableModel)tblImoveis.getModel()).getValueAt(tblImoveis.getSelectedRow(), 0);
            try {
                Imovel c = controle.getImovelPorId((Integer) valor);
                txtNome.setText(c.getNome());
                txtDescricao.setText(c.getDescricao());
                txtAreaTotal.setText(String.valueOf(c.getAreatotal()));
                txtAreaConst.setText(String.valueOf(c.getAreaconst()));
                cboTipo.setSelectedItem(c.getTipo());
                
                habilitarCampos();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }else{
            JOptionPane.showMessageDialog(this, "Selecione uma linha da tabela.");
        }

    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        ImovelControle controle = new ImovelControle();

        try {
            Imovel c = new Imovel();
            c.setNome(txtNome.getText());
            c.setDescricao(txtDescricao.getText());
            c.setAreatotal(Long.parseLong(txtAreaTotal.getText()));
            c.setAreaconst(Long.parseLong(txtAreaConst.getText()));
            c.setTipo((Tipo) cboTipo.getSelectedItem());
            if(tblImoveis.getSelectedRow()>=0){
            c.setId((Integer) tblImoveis.getValueAt(tblImoveis.getSelectedRow(), 0));
        }
        
            controle.gravar(c);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }catch(Exception ex2){
            JOptionPane.showMessageDialog(this, ex2.getMessage());
        }
        
        limparCampos();
        desabilitarCampos();
        atualizarLista();
    }//GEN-LAST:event_btnGravarActionPerformed

